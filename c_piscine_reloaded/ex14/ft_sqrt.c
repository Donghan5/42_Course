int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	if (nb > 0)
	{
		while ((sqrt * sqrt) <= nb)
		{
			if ((sqrt * sqrt) == nb)
				return (sqrt);
			if (sqrt > 46360)
				return (0);
			sqrt++;
		}
	}
	return (0);
}
